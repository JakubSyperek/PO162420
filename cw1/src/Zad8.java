public class Zad8 {
    public static void main(String[] args) {
        char a = 'a';
        char z = 'z';
        char a2 = 'A';
        char z2 = 'Z';
        char o = '0';
        char p = '9';

        int ia = a;
        int iz = z;
        int ia2 = a2;
        int iz2 = z2;
        int io = o;
        int ip = p;

        System.out.println("Małe litery: od " + ia + " do " + iz);
        System.out.println("Duże litery: od " + ia2 + " do " + iz2);
        System.out.println("Liczby: od " + io + " do " + ip);
    }
}
